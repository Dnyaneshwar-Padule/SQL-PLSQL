/*

    The drop command is DDL (Data Definition Language) command

    It can be used to drop (entirly delete) a database or a table.

    If we use drop command to drop a database, tha database is removed all with it's tables

    and if we use it to drop the table, the table is removed with all it's records.

    * Syntax.
        DROP TABLE table_name;
        DROP DATABASE database_name;

    * ex.
*/
        DROP TABLE student; 
        DROP DATABASE testdb;