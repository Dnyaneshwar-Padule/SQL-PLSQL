/* 

    We can rename a table with ALTER command
*/

    /*
        MySQL: 

        * Syntax - 
            ALTER TABLE current_table_name
            RENAME TO new_table_name;
    */

    /*
        PostgreSQL:

        * Syntax -
            ALTER TABLE current_table_name
            RENAME TO new_table_name;

    */


    /* Example */

    ALTER TABLE student
    RENAME to stu;